fn main() {
    input! {
        n: usize,
        w: usize,
        mut ab: [(usize, usize); n],
    }
    ab.sort_by_key(|&(a, _)| Reverse(a));
    let mut curr = 0;
    let mut ans = 0;
    for &(a, b) in ab.iter() {
        if curr + b > w {
            ans += (w - curr) * a;
            break;
        }
        ans += b * a;
        curr += b;
    }
    println!("{}", ans);
}
